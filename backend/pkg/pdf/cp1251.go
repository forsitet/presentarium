// utf8ToCP1251 converts a UTF-8 string to Windows-1251 (Cyrillic) encoded bytes.
// Characters that cannot be represented are replaced with '?'.
func utf8ToCP1251(s string) []byte {
	runes := []rune(s)
	out := make([]byte, 0, len(runes))
	for _, r := range runes {
		if r < 0x80 {
			out = append(out, byte(r))
			continue
		}
		if b, ok := unicodeToCP1251[r]; ok {
			out = append(out, b)
		} else {
			out = append(out, '?')
		}
	}
	return out
}

